package Beans;

public class Treino {
    
    private int ID_Treino;
    
    private String nomeTreino;
    private String descricaoTreino;
    private String tempoDescanco;

    public int getID_Treino() {
        return ID_Treino;
    }

    public void setID_Treino(int ID_Treino) {
        this.ID_Treino = ID_Treino;
    }

    public String getNomeTreino() {
        return nomeTreino;
    }

    public void setNomeTreino(String nomeTreino) {
        this.nomeTreino = nomeTreino;
    }

    public String getDescricaoTreino() {
        return descricaoTreino;
    }

    public void setDescricaoTreino(String descricaoTreino) {
        this.descricaoTreino = descricaoTreino;
    }

    public String getTempoDescanco() {
        return tempoDescanco;
    }

    public void setTempoDescanco(String tempoDescanco) {
        this.tempoDescanco = tempoDescanco;
    }
    
    
    
    
}
