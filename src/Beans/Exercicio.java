package Beans;


public class Exercicio {
    
    private int ID_Exercicio;
    
    private String nomeExercicio;
    private String descricaoExercicio;
    private String link1;
    private String link2;
    private String nomeQuantidade;

    public int getID_Exercicio() {
        return ID_Exercicio;
    }

    public void setID_Exercicio(int ID_Exercicio) {
        this.ID_Exercicio = ID_Exercicio;
    }

    public String getNomeExercicio() {
        return nomeExercicio;
    }

    public void setNomeExercicio(String nomeExercicio) {
        this.nomeExercicio = nomeExercicio;
    }

    public String getDescricaoExercicio() {
        return descricaoExercicio;
    }

    public void setDescricaoExercicio(String descricaoExercicio) {
        this.descricaoExercicio = descricaoExercicio;
    }

    public String getLink1() {
        return link1;
    }

    public void setLink1(String link1) {
        this.link1 = link1;
    }

    public String getLink2() {
        return link2;
    }

    public void setLink2(String link2) {
        this.link2 = link2;
    }

    public String getNomeQuantidade() {
        return nomeQuantidade;
    }

    public void setNomeQuantidade(String nomeQuantidade) {
        this.nomeQuantidade = nomeQuantidade;
    }
    
    
            
    
}
